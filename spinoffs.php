<p>
The Sarah Jane Adventures was created as a children's spinoff of the Doctor Who series. It ran for 5 seasons between 2007-2011 and was centered around Sarah Jane Smith, 
who was a full time companion of the third and fourth Doctors. She even made appearances during the tenth and eleventh Doctor's adventures. Her adventures revolve around 
strange, potential alien happenings in which her son and his friends help her investigate and solve the problems.    
</p>
<ul class="list">
    <li>Mr Smith
