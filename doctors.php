<h3> Who is the Doctor?</h3>
<p>
The Doctor is part an alien race called the Time Lords, who lived on the planet Gallifrey, and he is the last of his kind. The Time Lords were destroyed by the Daleks 
in the Last Great Time War. He travels through time and space using his TARDIS, time and relative dimension in space, to save innocents. In the reboot of the show, his 
most frequently used tool is his sonic screwdriver. Most often he travels with companions, who he meets during his adventures. The Doctor's name has yet to have been said during
the series and the only person who knows it is River Song, his eventual wife.  
